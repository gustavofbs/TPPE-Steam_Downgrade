    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String nome = txtNome.getText();
        float valor = Float.parseFloat(txtValor.getText());
        int classificacaoindicativa = Integer.parseInt(txtCi.getText());
        String desenvolvedor = txtDes.getText();
        String distribuidora = txtDis.getText();
        String requisitos = txtReq.getText();
        String genero = txtGen.getText();
        String descricao = txtDesc.getText();

        Jogo objjogo = new Jogo(nome,valor,classificacaoindicativa,desenvolvedor,distribuidora,requisitos,genero,descricao);

        JogoDAO objusuariodao = new JogoDAO();
        objusuariodao.cadastrarJogo(objjogo);
        
        /*coloca a imagem*/
         try {
            Imagem obj = new Imagem();
            obj.setImagem(ManipularImagem.getImgBytes(imagem));
            ImagemDAO dao = new ImagemDAO();
            Boolean foi = dao.inserir(obj);
            
            Telacadastrarjogo x = new Telacadastrarjogo();
            this.dispose();
            x.setVisible(true);
            

        } catch (Exception ex) {}
        JOptionPane.showMessageDialog(null, "Jogo cadastrado", "Sucesso", 1);
    }//GEN-LAST:event_jButton1ActionPerformed

